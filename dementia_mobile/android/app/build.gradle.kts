fun readMapsApiKey(): String {
    val envFile = rootProject.file("../../server/.env")
    if (!envFile.exists()) {
        return ""
    }

    val props = Properties()
    envFile.inputStream().use { props.load(it) }
    return props.getProperty("GOOGLE_MAPS_API_KEY", "").trim()
}

val mapsApiKey = readMapsApiKey()
if (mapsApiKey.isBlank()) {
    println("WARNING: GOOGLE_MAPS_API_KEY is missing in ../../server/.env. Google Maps may render a blank grid.")
}

